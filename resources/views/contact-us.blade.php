                                <script>
                                    document.getElementById('contactForm').addEventListener('submit', function(e) {
                                        e.preventDefault();

                                        // Disable the submit button
                                        document.getElementById('submitBtn').disabled = true;
                                        document.getElementById('loading').style.display = 'inline-block';
                                        document.getElementById('overlay').style.display = 'block';

                                        const form = new FormData(this);

                                        // Send form data to the backend
                                        axios.post("{{ route('contact.store') }}", form)
                                            .then(response => {
                                                // Show success message
                                                showResponseMessage('Form submitted successfully!', 'success');
                                            })
                                            .catch(error => {
                                                // Show error message
                                                showResponseMessage('Accept Terms and Conditions . Please try again.', 'error');
                                            })
                                            .finally(() => {
                                                // Re-enable the submit button and hide the spinner/overlay
                                                document.getElementById('submitBtn').disabled = false;
                                                document.getElementById('loading').style.display = 'none';
                                                document.getElementById('overlay').style.display = 'none';
                                            });
                                    });

                                    function showResponseMessage(message, type) {
                                        let responseMessage = document.getElementById('responseMessage');
                                        responseMessage.style.display = 'block';
                                        responseMessage.innerText = message;

                                        if (type === 'success') {
                                            responseMessage.style.color = 'green';
                                        } else {
                                            responseMessage.style.color = 'red';
                                        }

                                        setTimeout(() => {
                                            responseMessage.style.display = 'none';
                                        }, 5000); // Hide message after 5 seconds
                                    }
                                </script>

